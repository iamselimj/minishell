#include "../../inc/libft.h"

int	ft_putnbr_uns(unsigned int n)
{
	char c;

	if (n >= 10)
		ft_putnbr_uns(n / 10);
	c = (n % 10) + '0';
	write(1, &c, 1);
	return (0);
}