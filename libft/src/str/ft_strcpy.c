#include "libft.h"

/**
 * @brief Copies `src` into `dest`, including the null terminator.
 *
 * @param dest (char *) : Destination buffer.
 * @param src (const char *) : Source string.
 *
 * @return (char *) : Pointer to `dest`.
 */
char	*ft_strcpy(char *dest, const char *src)
{
	size_t	i;

	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
